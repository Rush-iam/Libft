#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char const	c = '0' + n % 10 * ((n > 0) - (n < 0));

	n < 0 ? write(fd, "-", 1) : 0;
	(n /= 10 * ((n >= 0) - (n < 0))) ? ft_putnbr_fd(n, fd) : 0;
	write(fd, &c, 1);
}
